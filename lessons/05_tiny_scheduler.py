"""Урок 05 — кооперативная многозадачность на одном потоке.

Учит: несколько корутин по очереди работают в ОДНОМ потоке — планировщик крутит их
через .send(), а `await Yield()` — это добровольная заявка «уступаю ход, верни меня в очередь».
Ага-момент: конкурентность БЕЗ потоков; но пока корутина не уступила (нет await) — все ждут.
"""

from collections import deque
from typing import Any, Generator


# =====================================================================
# ЧАСТЬ 1. Awaitable «уступаю ход»
# =====================================================================


class Yield:
    """`await Yield()` — кооперативная заявка «я готов подождать, дай поработать другим».

    Внутри `__await__` мы делаем ОДИН пустой `yield` (из уроков 02-03: это выход наружу
    к тому, кто нас крутит). Планировщик увидит эту заявку, вернёт корутину в конец
    очереди и продолжит с того же места на следующем круге. Значение мы не отдаём и не
    ждём — важен сам факт «я уступил».
    """

    def __await__(self) -> Generator[None, Any, None]:
        # `yield` без значения = отдать наружу None и ЗАМЕРЕТЬ здесь.
        # Управление уходит в планировщик; когда он снова сделает .send(...) —
        # мы продолжим со следующей строки (её нет, поэтому просто вернёмся из await).
        yield


# =====================================================================
# ЧАСТЬ 2. Корутины-работники: делают несколько шагов, уступая ход после каждого
# =====================================================================


async def worker(name: str, rounds: int) -> None:
    """Работник: делает `rounds` шагов, после каждого шага добровольно уступает ход.

    Ключ к чередованию — `await Yield()` внутри цикла: корутина сама решает,
    в какой момент она готова отдать управление соседям.
    """
    for step in range(1, rounds + 1):
        # Полезная работа этого шага (у нас — просто печать, чтобы видеть чередование).
        print(f"    [{name}] делаю шаг {step} из {rounds}")
        # А ВОТ ТУТ уступаем ход: планировщик поставит нас в конец очереди
        # и сначала прокрутит остальных, и только потом вернётся к нам на step+1.
        await Yield()

    print(f"    [{name}] все {rounds} шага сделаны — ЗАВЕРШАЮСЬ")


# =====================================================================
# ЧАСТЬ 3. Планировщик: очередь готовых корутин + круговой обход через send
# =====================================================================


def run(tasks: list[tuple[str, Any]]) -> None:
    """Крошечный планировщик кооперативных корутин на одном потоке.

    tasks — список пар (имя, корутина). Имя нужно только для наглядных print'ов;
    сама логика планировщика ничего не знает о внутренностях корутин.
    Правило простое:
      1) достаём корутину из головы очереди;
      2) толкаем её .send(None) — она работает до ближайшего `await Yield()` (или до конца);
      3) если уступила ход (выпал yield) — кладём в ХВОСТ очереди (отсюда и чередование);
      4) если завершилась (StopIteration) — выкидываем из очереди.
    Когда очередь пуста — работать некому, выходим.
    """
    ready = deque(tasks)  # очередь «готовых бежать» корутин (FIFO — отсюда честный круг)
    tick = 0
    while ready:
        tick += 1
        name, coro = ready.popleft()  # берём того, чья очередь подошла
        print(f"[планировщик] тик {tick}: даю ход корутине {name!r}")

        try:
            # ОДИН send прокручивает корутину до её следующего await Yield() либо до return.
            # Первый send обязан быть None (прогрев из урока 02); дальше тоже None —
            # мы ничего не возвращаем в await, нам важен лишь сам момент передачи хода.
            coro.send(None)
        except StopIteration:
            # Корутина добежала до конца тела — НЕ возвращаем её в очередь.
            print(f"[планировщик]   {name!r} закончила работу — убираю из очереди")
            continue

        # Сюда попадаем, только если корутина сделала await Yield() (уступила ход).
        # Возвращаем её в ХВОСТ — пусть сначала поработают те, кто ждал перед ней.
        print(f"[планировщик]   {name!r} уступила ход — возвращаю в конец очереди")
        ready.append((name, coro))

    print(f"[планировщик] очередь пуста за {tick} тиков — планировать больше некого")


def part1_fair_interleaving() -> None:
    """Запускаем двух работников и видим честное чередование A, B, A, B, ..."""
    print("=== Часть 1. Два потока управления в ОДНОМ потоке ОС ===")
    print("Запускаем worker('A') и worker('B'). Каждый уступает ход после каждого шага,")
    print("поэтому планировщик крутит их по кругу: сначала A, потом B, снова A, снова B...\n")

    run([
        ("A", worker("A", rounds=3)),
        ("B", worker("B", rounds=3)),
    ])

    print("\nВЫВОД: шаги A и B ПЕРЕМЕЖАЮТСЯ, хотя потоков/процессов нет — один стек, один поток.")
    print("Это кооперативная многозадачность: корутины САМИ уступают ход на await.\n")


# =====================================================================
# ЧАСТЬ 4. Проблема №1: большая задача без await стопорит весь loop
# =====================================================================


async def hog(name: str, chunk: int) -> None:
    """«Прожорливая» корутина: делает много работы ПОДРЯД, БЕЗ единого await внутри.

    Пока она не дойдёт до `await Yield()` в самом конце — управление к планировщику
    не вернётся, а значит, никто другой в это время не побежит.
    """
    print(f"    [{name}] старт большой задачи: {chunk} единиц работы БЕЗ await между ними")
    total = 0
    for i in range(1, chunk + 1):
        total += i  # представь тут тяжёлый CPU-расчёт: парсинг, шифрование, ML-инференс
        print(f"    [{name}]   перемалываю единицу {i}/{chunk} — сосед в это время СТОИТ")

    # Уступаем ход ТОЛЬКО СЕЙЧАС — вся работа выше прошла одним неделимым куском.
    print(f"    [{name}] большой кусок готов (сумма={total}); вот теперь уступаю ход")
    await Yield()
    print(f"    [{name}] получил ход обратно — завершаюсь")


def part2_blocking_problem() -> None:
    """Показываем будущую боль: корутина без await не даёт соседу ни одного тика."""
    print("=== Часть 2. Проблема №1: тяжёлая задача без await стопорит весь loop ===")
    print("Ставим в очередь hog (грызёт большой кусок без await) и лёгкого worker('C').")
    print("Смотри: ВСЕ строки hog напечатаются ДО первого шага C — C голодает, пока hog занят.\n")

    run([
        ("hog", hog("hog", chunk=5)),
        ("C", worker("C", rounds=2)),
    ])

    print("\nВЫВОД: планировщик отдаёт управление только НА await. Нет await — нет переключения.")
    print("Один поток: пока hog крутит свой цикл, планировщик заморожен внутри его .send().")
    print("Отсюда правило asyncio: тяжёлую/блокирующую работу нельзя делать прямо в корутине —")
    print("её выносят в отдельный поток/процесс (executor) — вернёмся к этому в уроке 09.\n")


def main() -> None:
    print("Кооперативная многозадачность: много корутин, один поток, ноль магии\n")
    part1_fair_interleaving()
    part2_blocking_problem()

    print("ИТОГ УРОКА:")
    print("  1) планировщик = очередь готовых корутин + цикл вокруг .send();")
    print("  2) await Yield() — добровольная заявка «уступаю ход», корутина уходит в хвост очереди;")
    print("  3) так получается конкурентность БЕЗ потоков — шаги разных корутин чередуются;")
    print("  4) НО переключение происходит только на await: большая задача без await стопорит всех.")


if __name__ == "__main__":
    main()


