"""Урок 02 — yield как двусторонний канал (механика await).

Учит: `x = yield y` работает в ОБЕ стороны: `yield y` отдаёт y наружу
вызывающему .send(), а значение из .send(v) возвращается ВНУТРЬ как результат (x = v).
Ага-момент: это ровно то, что делает await — тело отдаёт наружу «заявку» и получает назад результат.
"""

from typing import Any, Generator


# ---------------------------------------------------------------------------
# Часть 1. Генератор-«эхо»: принимает через .send(), отвечает через yield.
# ---------------------------------------------------------------------------

def echo_machine() -> Generator[str, Any, None]:
    """Генератор-эхо: на каждом круге отдаёт наружу заготовленный ответ
    и принимает внутрь новое значение из .send()."""
    # Важно: этот код НЕ выполнится, пока кто-то снаружи не «толкнёт» генератор
    # первый раз (через next() или .send(None)). До первого yield тело спит.
    print("  [внутри] генератор проснулся, дошёл до первого yield")

    answer = "готов принимать"  # что отдадим наружу на самом ПЕРВОМ yield
    while True:
        # Одна строка — два действия подряд:
        #   1) `yield answer`  — отдаёт answer наружу, вызов .send()/next() возвращает его;
        #   2) генератор ЗАМИРАЕТ на этой строке;
        #   3) когда снаружи сделают .send(v) — v становится значением ВСЕГО выражения,
        #      и присваивается в incoming (это и есть `x = yield y`).
        incoming = yield answer
        print(f"  [внутри] пришло через send(): {incoming!r} — готовлю эхо-ответ")
        answer = f"эхо<{incoming}>"  # ответ отдастся наружу на следующем витке yield


# ---------------------------------------------------------------------------
# Часть 2. Правило «первый send обязан быть None» (priming — «прогрев»).
# ---------------------------------------------------------------------------

def priming_rule_demo() -> None:
    """Показываем, почему самый первый .send() должен быть None."""
    print("\n=== Часть 2. Почему первый send обязан быть None ===")
    print("Генератор ещё НЕ стоит на yield — ему некуда положить присланное значение,")
    print("поэтому Python запрещает первый .send() с чем-либо кроме None.\n")

    gen = echo_machine()
    print("[снаружи] пробуем сразу gen.send('привет') БЕЗ прогрева...")
    try:
        gen.send("привет")
    except TypeError as exc:
        # Ровно та ошибка, которую все ловят в первый день знакомства с корутинами.
        print(f"[снаружи] поймали TypeError: {exc}")

    print("\n[снаружи] правильный прогрев — gen.send(None) (то же, что next(gen)):")
    first = gen.send(None)
    print(f"[снаружи] теперь генератор стоит на yield и вернул: {first!r}")
    gen.close()


# ---------------------------------------------------------------------------
# Часть 3. Табличка шагов «снаружи / внутри» — видно оба направления канала.
# ---------------------------------------------------------------------------

def echo_table_demo() -> None:
    """Гоняем эхо-генератор и печатаем табличку: что делаем снаружи и что вернулось."""
    print("\n=== Часть 3. Табличка двустороннего обмена ===")
    gen = echo_machine()

    rows: list[tuple[str, str, str]] = []

    # Шаг 0 — прогрев. Отправить можно только None: генератор ещё не на yield.
    out = gen.send(None)
    rows.append(("gen.send(None)", "стартует, встаёт на yield", repr(out)))

    # Шаги 1..3 — реальный диалог. Значение из send становится результатом `yield` внутри.
    for word in ["раз", "два", "три"]:
        out = gen.send(word)
        inside = f"incoming = {word!r}"
        rows.append((f"gen.send({word!r})", inside, repr(out)))

    gen.close()  # генератор с while True сам не остановится — закрываем явно

    # Печатаем ровную табличку (выравнивание тут — это ФОРМАТ вывода, а не код).
    header = ("СНАРУЖИ (что вызвали)", "ВНУТРИ (что произошло)", "ВЕРНУЛОСЬ наружу")
    widths = (22, 30, 18)
    line = "+".join("-" * (w + 2) for w in widths)
    print(line)
    print(f"| {header[0]:<{widths[0]}} | {header[1]:<{widths[1]}} | {header[2]:<{widths[2]}}")
    print(line)
    for outside, inside, result in rows:
        print(f"| {outside:<{widths[0]}} | {inside:<{widths[1]}} | {result:<{widths[2]}}")

    print(line)
    print("Читай так: одно значение УХОДИТ наружу (yield), другое ВХОДИТ внутрь (send).")


# ---------------------------------------------------------------------------
# Часть 4. Ага-момент: это ТОЧНО механика await.
# ---------------------------------------------------------------------------

class AwaitRequest:
    """Мини-модель «заявки», как в fiber-рантайме actflow.
    `__await__` отдаёт саму заявку наружу и возвращает то, что рантайм пришлёт назад."""

    def __init__(self, name: str):
        self.name = name

    def __await__(self):
        # Вот он — тот же самый двусторонний yield, что и в эхо-генераторе:
        #   `yield self` отдаёт заявку наружу центральному циклу;
        #   result — это то, что цикл вернёт через .send(), когда заявка будет исполнена.
        result = yield self
        return result


async def fiber_body() -> str:
    """«Тело файбера»: просит рантайм посчитать что-то и ждёт результат."""
    print("  [файбер] отдаю наружу заявку 'sum(2,3)' и жду ответ...")
    answer = await AwaitRequest("sum(2,3)")
    print(f"  [файбер] рантайм вернул мне: {answer}")
    return f"итог = {answer}"


def tiny_runtime_demo() -> None:
    """Крошечный «event loop»: сам крутит корутину через .send() и исполняет заявки."""
    print("\n=== Часть 4. Тот же yield лежит под await ===")
    print("Корутина — это тоже генератор: await внутри разворачивается в `yield заявка`.")
    print("Наш игрушечный цикл общается с ней ровно как с эхо-генератором — через .send().\n")

    coro = fiber_body()
    to_send: Any = None  # первый send обязан быть None — то же правило прогрева!
    while True:
        try:
            # Толкаем корутину: получаем наружу либо заявку (yield), либо StopIteration с результатом.
            request = coro.send(to_send)
        except StopIteration as done:
            print(f"[цикл] корутина завершилась, её return-значение: {done.value!r}")
            break

        # Наружу «выпала» заявка — исполняем её и готовим ответ, чтобы отправить назад внутрь.
        print(f"[цикл] получил заявку {request.name!r}, исполняю и отправляю результат обратно")
        to_send = 5  # «результат» заявки; уйдёт внутрь и станет значением `await`


def main() -> None:
    print("=== Часть 1. Генератор-эхо: yield отдаёт наружу, send кладёт внутрь ===")
    gen = echo_machine()
    print("[снаружи] прогрев next(gen) — доводим тело до первого yield:")
    greeting = next(gen)
    print(f"[снаружи] генератор отдал наружу: {greeting!r}")
    print("[снаружи] gen.send('мир') — значение 'мир' войдёт внутрь как результат yield:")
    reply = gen.send("мир")
    print(f"[снаружи] в ответ через yield пришло: {reply!r}")
    gen.close()

    priming_rule_demo()
    echo_table_demo()
    tiny_runtime_demo()

    print("\nВЫВОД: `x = yield y` — это труба на два конца.")
    print("Наружу уходит y (заявка), внутрь возвращается x (ответ). await устроен так же.")


if __name__ == "__main__":
    main()


